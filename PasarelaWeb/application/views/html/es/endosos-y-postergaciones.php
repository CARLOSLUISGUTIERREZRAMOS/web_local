                    <h1>Endosos y postergaciones</h1>
                    <p align="center"></p>
                    <h3>Condiciones para endosos, postergación y tramos múltiples de pasajes aéreos.</h3><br>
                    <p>En Cumplimiento con lo establecido en la Ley 29571, Código de Protección y Defensa del Consumidor y su modificatoria, Ley N° 30046, StarPerú informa sobre las condiciones para endosos, postergación y tramos múltiples de pasajes aéreos.</p>
                    <h2>Para  los casos de Endosos:</h2>
                    <ul>
                    <li><p class="lead">1. El Endoso se permite en billetes de pasaje 100% pagados, en todas las rutas nacionales.</p></li>
                    <li><p class="lead">2. El trámite se deberá realizar personalmente, presentándose el titular y el beneficiario del billete de pasaje debidamente identificado. Deberán presentar una solicitud de endoso con firma y copia del documento de identidad del titular y beneficiario.</p></li>
                  <li><p class="lead">3. En el caso de boletos con RUC, se deberá presentar carta original membretada de la empresa, suscrita por su representante legal y solicitando el endoso. El nuevo boleto deberá contener el mismo RUC de la empresa, ya que ésta es su titular.</p></li>
                  <p class="lead">En ambos casos, el trámite deberá realizarse en las oficinas comerciales de StarPerú, durante el horario de atención y con un mínimo de 24 horas antes de la salida del vuelo inicial. Este trámite no se realiza en Aeropuertos.
                  </p><li><p class="lead">4. Se permite el endoso por billete de pasaje:</p></li>
                    <ul>
                        <li>
                            <p class="lead">Respetando las mismas condiciones pactadas del billete.</p>
                        </li>
                        <li>
                            <p class="lead">Asumiendo gasto de emisión por nuevo boleto de US$ 17.70 (incluido IGV) o su equivalente en moneda nacional.</p>
                        </li>
                    </ul>
                  </ul>
                <h2>Para  los casos de Postergaciones:</h2>
                    <ul>
                    <li><p class="lead">1. La postergación se permite en billetes de pasaje 100% pagados, en todas las rutas nacionales.</p></li>
                    <li><p class="lead">2. El trámite se deberá realizar en las oficinas comerciales de StarPerú, en la agencia de viaje donde adquirió su billete de pasaje o a través de nuestro call center.</p></li>
                    <li><p class="lead">3. El cambio procederá:</p></li>
                        <ul>
                        <li>
                            <p class="lead">Respetando las mismas condiciones pactadas del billete de pasaje.</p>
                        </li>
                        <li>
                            <p class="lead">Podrá  ser considerado como parte de pago según lo pactado.</p>
                        </li>
                        <li>
                            <p class="lead">La postergación estará sujeta a disponibilidad en la misma clase tarifaria; caso contrario, se considerará el billete de pasaje como parte de pago de una tarifa superior, debiendo pagarse la diferencia tarifaria. Podrá buscar la fecha en que la clase original se encuentre disponible para evitar el pago de diferencia de tarifa.</p>
                        </li>
                        <li>
                            <p class="lead">Asumiendo gasto de emisión por nuevo boleto de US$ 17.70 (incluido IGV) o su equivalente en moneda nacional.</p>
                        </li>
                        </ul>
                            <p class="lead">Como política interna de StarPerú, se podrá  realizar la postergación más allá del plazo señalado por Ley, bajo las  siguientes condiciones:</p>
                        <ul>
                        <li>
                            <p class="lead">Asumiendo gasto de emisión por nuevo boleto de US$ 17.70 (incluido IGV) más IGV o su equivalente en moneda, más una penalidad equivalente a US$ 17.70 (incluido IGV) o su equivalente en moneda nacional; sin perjuicio de considerar el billete de pasaje como parte de pago de una tarifa superior, debiendo pagarse la diferencia tarifaria, en caso no exista la misma.</p>
                        </li>
                    </ul>
                            <p class="lead">Nota: En caso de solicitarse la postergación del servicio (cambio de fecha) y el cambio de nombre (endoso) en un mismo billete de pasaje, se efectuará un solo cargo de US$ 17.70 (incluido IGV) o su equivalente en moneda nacional.</p>
                        
                    </ul>
                    <h2>Tramos  Múltiples:</h2>
                    <p>Todo pasajero que adquiera billetes de pasaje de ida y vuelta o billetes de pasaje para destinos o tramos múltiples y no hiciera uso de alguno de los tramos, tiene derecho de utilizar los destinos o tramos siguientes. Sin perjuicio de lo antes mencionado, se procederá conforme a la política de endosos y postergaciones en caso el pasajero decida efectuar estos cambios para los tramos restantes.</p>
                    <p>El  cambio procederá teniendo en cuenta: </p>
                    <ul>
                        <li>
                            <p class="lead">Validez  de la tarifa adquirida: contando a partir de la fecha de inicio de vuelo.</p>
                        </li>
                        <li>
                            <p class="lead">Validez  del billete de pasaje: 1 año contando a partir de su fecha de emisión original.</p>
                        </li>
                    </ul>
                    <p class="lead">(*) Los billetes de pasaje del Programa de viajero Frecuente (StarPass) se regirán por los procedimientos y condiciones del mismo.</p>
                    <h2>Cobros de fee de emision:</h2>
                    <p class="lead">Boleto OW: USD6.00 + IGV</p>
                    <p class="lead">Boleto RT: USD12.00 + IGV</p>
                    
