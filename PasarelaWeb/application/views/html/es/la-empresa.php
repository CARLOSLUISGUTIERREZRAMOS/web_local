                    <h1>La empresa</h1>
                    
                   
                    <p>StarPerú se fundó  en Mayo  de  1997 como Star Up S.A., con capitales peruanos y ucranianos.</p>
                    <p>Inició  operaciones brindando servicios de transporte de carga y vuelos charter para empresas nacionales y  transnacionales,  con   una   flota  conformada   por  aviones  Antonov,  de fabricación  soviética.</p>
                    <p>En el 2004, incorpora a su flota  el primer avión Boeing 737-200 y  se inician las  operaciones comerciales, teniendo como primer destino la ciudad de Pucallpa.</p>
                    <p>En el 2005, se adopta el nombre comercial de  StarPerú  y  progresivamente se van incorporando más aviones Boeing 737-200 hasta llegar a una flota de 5 aeronaves.</p>
                    <p>En el 2008, se decide renovar la flota con aviones más eficientes  y adecuados  a las características de nuestra geografía y de nuestros aeropuertos, para lo cual se eligió un avión de fabricación inglesa, el  BAe 146.</p>
                    <p>A inicios  del 2010 se culminó el  proceso de renovación de flota, retirando la última aeronave Boeing 737-200.</p>
                    <p>A mediados del mismo año, renovó su imagen corporativa, cambiando  su logotipo  y colores institucionales, dando paso a una nueva etapa de crecimiento.</p>
                    <p>En la actualidad, StarPerú opera a 8 destinos nacionales y cuenta con una flota compuesta por 9 aviones  BAe 146, de los cuales 8 son de transporte de pasajeros y 1 para transporte de carga.</p>
                    <p>StarPerú es una aerolínea joven, moderna y eficiente. con más de 450 colaboradores  comprometidos con brindar un servicio cálido y personalizado.</p>
                   
                  
