            <h2> Políticas de equipaje interlineal.</h2>
            <p>Para el transporte de sus equipajes bajo el acuerdo interlineal, franquicias de equipaje permitidas son:</p>
            <p>Equipaje de mano: 5 Kg.</p>
            <p>Equipaje Facturado: 25 Kg.</p> 
            <p>Cobro por exceso de equipaje: $12 incluyendo impuestos por  kilogramos.</p>

            <h2>Equipaje no permitido.</h2>

            <p>Por su condición, existen algunos artículos son considerados como mercancías peligrosas  y pueden afectar tu seguridad y la de todos los pasajeros, por lo que no se permiten llevar como equipaje. <a href="equipaje-no-permitido.html">(ver más información)</a></p>

            <h2>Armas.</h2>

            <p>StarPerú no aceptará armas en el equipaje de mano.
                Se aceptarán armas de defensa personal y serán embarcadas en los compartimentos de carga de la aeronave. <a href="armas.html">(ver más información)</a></p>

            <h2>Responsabilidad con el equipaje.</h2>

            <p>Te recomendamos no incluir dentro del equipaje facturado, artículos irremplazables, frágiles o de alto valor, estos deben ser transportados bajo tu custodia dentro del equipaje de mano o enviados a través de una empresa de carga. La responsabilidad de la aerolínea es limitada cuando estos no hayan sido declarados. <a href="responsabilidad.con-el-equipaje.html">(ver más información)</a></p>

