            <h1>Sobre mi equipaje</h1>

            <p>Se considera como equipaje: ropa, artículos y objetos personales necesarios para el vestir, uso o comodidad del pasajero en relación con su viaje, ya sea como equipaje de mano o registrado.</p>
            <p>Cualquier embalaje usado para empacar estos elementos debe incluir el nombre y dirección del cliente en la parte exterior, para facilitar su reconocimiento.</p>
            <p>A continuación te brindamos toda la información que debes saber sobre el transporte de tu equipaje al momento de viajar.</p>

            <h2>Equipaje de mano.</h2>

            <p>Son aquellos efectos personales libres de cargo transportados por el pasajero bajo su custodia y responsabilidad en la cabina del avión, en adición a la franquicia libre permitida. <a href="equipaje-de-mano.html">(ver más información)</a></p>

            <h2>Equipaje libre facturado.</h2>

            <p>El equipaje facturado es todo aquel que fue chequeado en el aeropuerto y que viaja en la bodega del avión. <a href="equipaje-libre-facturado.html">(ver más información)</a></p>

            <h2>Exceso de equipaje.</h2>

            <p>Cualquier excedente a la franquicia libre permitida, se considera como exceso de equipaje. Podrá ser transportado mediante el pago de 2.95 USD (inc.IGV) por cada kilo de exceso.</p>

